"""Joint identification of σ from the cleaned-benchmark gap and α*.

Per §7.1 of the AIJ manuscript, the model identity is:
    M − S = (1 − σ) α
where M is the public-metric score and S is the hidden-clean-metric score.
Equivalently:
    σ = 1 − (M − S) / α*

Joint identification requires two empirical anchors:
- `gap_hat`: M − S = public_accuracy − hidden_accuracy
- `alpha_hat`: equilibrium gaming intensity, from one of the 4 detector classes

This module aggregates per-model, per-strategy scores into the joint σ
estimate and reports per-detector sensitivity (does the σ conclusion depend
on which detector you trust?).
"""
from __future__ import annotations

from dataclasses import dataclass, field

import numpy as np

from phase_f.config import DETECTORS


@dataclass
class IdentificationResult:
    """σ estimate for one (model_nick, strategy_nick) pair.

    Per-detector σ values are reported separately so the framework's
    sensitivity to detector choice is visible.
    """
    model_nick: str
    strategy_nick: str
    public_accuracy: float
    hidden_accuracy: float
    paraphrased_accuracy: float | None
    gap: float                      # public − hidden = (1−σ)α
    alpha_hat_per_detector: dict[str, float]   # detector nick → α̂
    sigma_per_detector: dict[str, float]       # detector nick → 1 − gap/α̂
    sigma_lower_bound: float        # 1 − gap (assumes α=1, max gaming)
    sigma_upper_bound: float        # 1 − gap/min(α̂) (most-detected → least σ)
    sigma_central: float            # median over detectors with α̂ > 0
    n_items: int
    notes: list[str] = field(default_factory=list)


def identify_sigma(
    public_accuracy: float,
    hidden_accuracy: float,
    alpha_hat_per_detector: dict[str, float],
    *,
    model_nick: str,
    strategy_nick: str,
    paraphrased_accuracy: float | None = None,
    n_items: int = 0,
    eps: float = 1e-3,
) -> IdentificationResult:
    """Compute σ from observed (public, hidden) accuracies and per-detector α̂.

    `eps` is a floor on α̂ — detectors that return α̂ ≈ 0 imply the gap is
    NOT generated by voluntary gaming (= involuntary contamination diagnostic).
    For those detectors we record σ = NaN and add a note.
    """
    gap = public_accuracy - hidden_accuracy
    notes: list[str] = []

    sigma_per_detector: dict[str, float] = {}
    for det, alpha in alpha_hat_per_detector.items():
        if alpha < eps:
            sigma_per_detector[det] = float("nan")
            notes.append(
                f"detector={det}: α̂={alpha:.4f} ≈ 0; gap not explained by voluntary gaming "
                f"(involuntary-contamination diagnostic per §7.1)"
            )
            continue
        sigma_per_detector[det] = float(np.clip(1.0 - gap / alpha, -1.0, 1.0))

    # Central estimate: median over detectors with valid α̂
    valid = [v for v in sigma_per_detector.values() if not np.isnan(v)]
    sigma_central = float(np.median(valid)) if valid else float("nan")

    # Conservative bounds
    # Upper: α = 1 (max gaming) ⇒ σ = 1 − gap (the "all gap is leakage" interpretation)
    sigma_upper = float(np.clip(1.0 - gap, -1.0, 1.0))
    # Lower: use the smallest valid α̂ (= the detector that fires most heavily)
    alphas_valid = [a for a in alpha_hat_per_detector.values() if a >= eps]
    if alphas_valid:
        sigma_lower = float(np.clip(1.0 - gap / max(alphas_valid), -1.0, 1.0))
    else:
        sigma_lower = float("nan")

    if gap < 0:
        notes.append(
            f"gap={gap:.4f} < 0: public score LOWER than hidden — surprising; possible "
            f"that contamination did not improve public score (decontamination effort, "
            f"failed leak, or noise within ±2σ)"
        )

    return IdentificationResult(
        model_nick=model_nick,
        strategy_nick=strategy_nick,
        public_accuracy=public_accuracy,
        hidden_accuracy=hidden_accuracy,
        paraphrased_accuracy=paraphrased_accuracy,
        gap=gap,
        alpha_hat_per_detector=dict(alpha_hat_per_detector),
        sigma_per_detector=sigma_per_detector,
        sigma_lower_bound=sigma_lower,
        sigma_upper_bound=sigma_upper,
        sigma_central=sigma_central,
        n_items=n_items,
        notes=notes,
    )


def summarise_table(results: list[IdentificationResult]) -> str:
    """Pretty-printed table for CLI / log inspection."""
    lines = [
        f"{'model':14} {'strategy':22} {'pub':>6} {'hid':>6} {'gap':>7}  "
        + "  ".join(f"{d:>10}" for d in DETECTORS) + "  σ_central"
    ]
    for r in results:
        row = [
            f"{r.model_nick:14}",
            f"{r.strategy_nick:22}",
            f"{r.public_accuracy:6.3f}",
            f"{r.hidden_accuracy:6.3f}",
            f"{r.gap:+.4f}".rjust(7),
        ]
        for d in DETECTORS:
            v = r.sigma_per_detector.get(d, float("nan"))
            row.append(f"{v:>10.3f}" if not np.isnan(v) else f"{'nan':>10}")
        row.append(f"{r.sigma_central:.3f}" if not np.isnan(r.sigma_central) else "nan")
        lines.append("  ".join(row))
    return "\n".join(lines)
